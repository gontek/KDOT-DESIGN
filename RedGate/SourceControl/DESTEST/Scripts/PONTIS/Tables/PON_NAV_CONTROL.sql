  multi_line_rows NUMBER(38),
  tooltip_caption_id NUMBER(38),
  control_gui_resource_id VARCHAR2(256 BYTE),
  image_alignment VARCHAR2(32 BYTE),
  text_alignment VARCHAR2(32 BYTE),
  on_client_click VARCHAR2(256 BYTE),
  popup_task_id NUMBER(38),
  pontis_standard_ind CHAR,
  override_param_table_name VARCHAR2(30 BYTE),
  override_param_field_name VARCHAR2(30 BYTE),
  link_key NUMBER(4),
  CONSTRAINT pon_nav_control_pk PRIMARY KEY (tab_id,task_id,control_group_id,control_id) DISABLE NOVALIDATE,
  CONSTRAINT fk_nav_ctrl_to_caption FOREIGN KEY (label_caption_id) REFERENCES pontis.pon_app_caption (caption_id) DISABLE NOVALIDATE,
  CONSTRAINT fk_nav_ctrl_to_caption_two FOREIGN KEY (tooltip_caption_id) REFERENCES pontis.pon_app_caption (caption_id) DISABLE NOVALIDATE,
  CONSTRAINT fk_nav_ctrl_to_ctrlgrp FOREIGN KEY (tab_id,task_id,control_group_id) REFERENCES pontis.pon_nav_control_group (tab_id,task_id,control_group_id) DISABLE NOVALIDATE,
  CONSTRAINT fk_nav_ctrl_to_task FOREIGN KEY (tab_id,popup_task_id) REFERENCES pontis.pon_nav_task (tab_id,task_id) DISABLE NOVALIDATE
);