  tab_id NUMBER(38) NOT NULL,
  task_id NUMBER(38) NOT NULL,
  control_group_id NUMBER(38) NOT NULL,
  control_id NUMBER(38) NOT NULL,
  "NAME" VARCHAR2(32 BYTE) NOT NULL,
  label_caption_id NUMBER(38) NOT NULL,
  order_num NUMBER(38) NOT NULL,
  visible_ind CHAR NOT NULL,
  read_only_ind CHAR NOT NULL,
  table_name VARCHAR2(30 BYTE),
  column_name VARCHAR2(30 BYTE),
  control_type VARCHAR2(32 BYTE),
  height VARCHAR2(16 BYTE),
  field_width VARCHAR2(16 BYTE),
  label_width VARCHAR2(16 BYTE),
  value_color VARCHAR2(32 BYTE),
  value_bold_ind CHAR,
  label_color VARCHAR2(32 BYTE),
  label_bold_ind CHAR,
  show_unit_ind CHAR,
  skin_id VARCHAR2(64 BYTE),
  css_class VARCHAR2(64 BYTE),
  auto_postback_ind CHAR,
  postback_event_id VARCHAR2(64 BYTE),
  causes_validation_ind CHAR,
  missing_as_blank_ind CHAR,
  regex_error_message VARCHAR2(128 BYTE),
  required_field_error_message VARCHAR2(128 BYTE),
  range_error_message VARCHAR2(128 BYTE),
  range_minimum NUMBER(38),
  range_maximum NUMBER(38),
  validate_range_ind CHAR,
  validate_regex_ind CHAR,
  validate_required_field_ind CHAR,
  default_value_display VARCHAR2(64 BYTE),
  text_box_mode VARCHAR2(32 BYTE),
  tab_index NUMBER(38),
  horizontal_alignment VARCHAR2(32 BYTE),
  vertical_alignment VARCHAR2(32 BYTE),
  cell_padding NUMBER(38),
  cell_spacing NUMBER(38),
  wrap_text_ind CHAR,
  image_url VARCHAR2(256 BYTE),
  max_chars NUMBER(38),
  enable_view_state_ind CHAR,
